public class bitwiseOperations {
    public static void main(String[] args) {
        int ans = 5 & 6;
        System.out.println(ans);

        ans = 5 | 6;
        System.out.println(ans);

        ans = 5^6;
        System.out.println(ans);

        ans = ~ 5;
        System.out.println(ans);

        int ans1 = 16 << 2;
        System.out.println(ans1);

        ans = 10 >> 1;
        System.out.println(ans);
    }
}
